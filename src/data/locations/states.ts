export function findCities(state: string) {
  const stateFound = states.find((s) => {
    if (state.toLowerCase() === "fct" && s.name.toLowerCase() === "abuja")
      return true;

    return s.name === state;
  });
  if (stateFound)
    return stateFound.cities.map((c) => ({
      label: c.name,
      value: c.name.toLowerCase(),
    }));
  return [];
}
